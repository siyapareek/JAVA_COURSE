public class AssignmentOperator {
    public static void main(String[] args) {
        int p=20,q=10;
        // =
        p=q;
        System.out.println(p); //10
        // +=
        p+=q;
        System.out.println(p); //20
        // -=
        p-=q;
        System.out.println(p); //10
        // /=
        p/=q;
        System.out.println(p); //1
        // %=
        p%=q;
        System.out.println(p); //1

    }
}
